// 할당연산자 (Assignment Operators)

let a = 1;
a = a + 2;

console.log(a); // 3

//산술연산자를 축약해서 사용할 수 있다.

// a = a + 2; 의 축약버전
a += 2;
console.log(a); // 5

// a = a - 2; 의 축약버전
a -= 2;
console.log(a); // 3

// a = a * 2; 의 축약버전
a *= 2;
console.log(a); // 6

// a = a / 2; 의 축약버전
a /= 2;
console.log(a); // 3
